def soma(x: int=0, y: int=0) -> int:
    '''Soma de dois numeros inteiros
        
        Parameters
        ----------
        x: int
        y: int
    ''' 

    return x+y

def cubo()-> int:
    '''Faz o calculo de um numero inteiro ao cubo ao ser inserido pelo input'''

    numero = int(input('Numero a ser elevado ao cubo:'))
    
    return numero**3

